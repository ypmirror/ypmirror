            <p style="font-weight: bold">What does this lossy/lossless compression mean?!</p>
            <p>
                The lossless compression, as the name implies, means that after compressing the video, and then decompressing it,
                you wind up with the exact same data as you put in. A lossy compression is when you compress the video and, after
                decompressing it, you do not get back what you put in. Note that we're exact at 100% when we mean lossy/lossless,
                and color correction itself mean loss !
            </p>
